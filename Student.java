package week8;

public class Student {
    private String name;
    private int age;
    
 // Constructor
    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }
    
    // Getter for name
    public String getName() {
        return name;
    }
    
 // Getter for age
    public int getAge() {
        return age;
    }

}